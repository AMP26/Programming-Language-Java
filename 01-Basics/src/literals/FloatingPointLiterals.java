package literals;

public class FloatingPointLiterals {
    public static void main(String[] args) {
        double pi = 3.14159;
        float e = 2.718f; // 'f' is used here to indicate float literal

        System.out.println("Floating-point Literals:");
        System.out.println("Pi (double): " + pi);
        System.out.println("Euler's number (float): " + e);
    }
}

