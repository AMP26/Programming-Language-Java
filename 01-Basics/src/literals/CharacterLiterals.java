package literals;

// CharacterLiterals.java
public class CharacterLiterals {
    public static void main(String[] args) {
        // Character Literals
        char letterA = 'A';
        char unicodeChar = '\u0041'; // Unicode representation of 'A'

        // Output
        System.out.println("Character Literals:");
        System.out.println("Character 'A': " + letterA);
        System.out.println("Unicode 'A': " + unicodeChar);
    }
}