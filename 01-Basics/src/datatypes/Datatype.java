package datatypes;

public class Datatype {
    // Integral Datatypes
    public byte a = 10; // Range: -128 to 127 [-2e7 to 2e7-1]
    public short b = 20; // Range: -32768 to 32767 [-2e15 to 2e15-1]
    public int c = 30; // Range: -2147483648 to 2147483647 [-2e31 to 2e31-1]
    public long d = 40; // Range: -2e63 to 2e63-1

    // Floating-point Datatypes
    public float e = 50.3f; // Range: -3.4e38 to 3.4e38.
    public double f = 60.4; // Range: -1.7e308 to1.7e308

    // Boolean Datatype
    public boolean g = true;
    public boolean h = false;

    // Character Datatype
    char i = 'a'; // Range: 0 to 65535
}
