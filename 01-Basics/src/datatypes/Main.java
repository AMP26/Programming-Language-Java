package datatypes;

public class Main {
    public static void main(String[] args) {
        Datatype dt = new Datatype();

        System.out.println("byte: " + dt.a);
        System.out.println("short: " + dt.b);
        System.out.println("int: " + dt.c);
        System.out.println("long: " + dt.d);

        System.out.println("float: " + dt.e);
        System.out.println("double: " + dt.f);

        System.out.println("boolean: " + dt.g);
        System.out.println("boolean: " + dt.h);

        System.out.println("char: " + dt.i);
    }
}
